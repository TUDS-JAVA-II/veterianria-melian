    private void agregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarClienteActionPerformed
       /**
        * Metodo para agregar clientes a la bd desde un boton
        * id,dni,apellido,nombre,direccion,numFijo,numCelu,Mail
        */
       SqlUsuario modSql = new SqlUsuario();
       Cliente modCliente = new Cliente();
       
       modCliente.setNombreCliente(nombreCliente.getText());
       modCliente.setApellidoCliente(apellidoCliente.getText());
       modCliente.setDni(dniCliente.getText());
       modCliente.setDomicilio(domicilioCliente.getText());
       modCliente.setNumCelular(celularCliente.getText());
       modCliente.setNumFIjo(telFijoCliente.getText());
       modCliente.setEmail(emailCliente.getText());
       /*ACA ME RENDI, NO SE QUE PASA -.-'*/
       
       if (modSql.AgregarUsuario(modCliente)) { //validacion para agregar un usario a la db
                        JOptionPane.showMessageDialog(null, "Se guard√≥ exitosamente el usuario");
                        limpiar(); // funcion para borrar los campos de texto y dejarlos listos para volver a agregar un usario
                    } else {
                        JOptionPane.showMessageDialog(null, "Hubo un error en el registro de usuario");
                    }
    }//GEN-LAST:event_agregarClienteActionPerformed

